-- 2026년 리딩지저스 성경 읽기 일정 테이블

-- reading_schedules 테이블 생성 (존재하지 않는 경우)
CREATE TABLE IF NOT EXISTS reading_schedules (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  year INTEGER NOT NULL,
  date DATE NOT NULL,
  reading TEXT NOT NULL,
  memory_verse TEXT,
  is_supplement_week BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(year, date)
);

-- 월별 주제 테이블
CREATE TABLE IF NOT EXISTS monthly_themes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  year INTEGER NOT NULL,
  month INTEGER NOT NULL CHECK (month >= 1 AND month <= 12),
  theme TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(year, month)
);

-- 특별 일정 테이블
CREATE TABLE IF NOT EXISTS special_dates (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  date DATE NOT NULL,
  title TEXT NOT NULL,
  description TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(date)
);

-- RLS 정책
ALTER TABLE reading_schedules ENABLE ROW LEVEL SECURITY;
ALTER TABLE monthly_themes ENABLE ROW LEVEL SECURITY;
ALTER TABLE special_dates ENABLE ROW LEVEL SECURITY;

-- 모든 사용자가 읽기 가능
CREATE POLICY "reading_schedules_select" ON reading_schedules
  FOR SELECT USING (true);

CREATE POLICY "monthly_themes_select" ON monthly_themes
  FOR SELECT USING (true);

CREATE POLICY "special_dates_select" ON special_dates
  FOR SELECT USING (true);

-- 관리자만 수정 가능
CREATE POLICY "reading_schedules_admin_all" ON reading_schedules
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE id = auth.uid()
      AND role IN ('superadmin', 'admin')
    )
  );

CREATE POLICY "monthly_themes_admin_all" ON monthly_themes
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE id = auth.uid()
      AND role IN ('superadmin', 'admin')
    )
  );

CREATE POLICY "special_dates_admin_all" ON special_dates
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE id = auth.uid()
      AND role IN ('superadmin', 'admin')
    )
  );

